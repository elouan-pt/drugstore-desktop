    private void deleteSaleButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteSaleButtonMouseClicked
        int option = JOptionPane.showConfirmDialog(this, "¿Desea cancelar la venta con comprobante " + proofOfPaymentTextField.getText() + "?");
        if (option == JOptionPane.OK_OPTION) {
            boolean check = ProofOfPaymentDAO.deleteProofOfPayment(sale.getProofOfPayment(), Constants.getUserLoged());
            if (check) {
                for (SaleProductDetail spd : list) {
                    spd.getProduct().setStock(spd.getQuantity());
                    check &= ProductDAO.getProduct(spd.getProduct());
                }
                JOptionPane.showMessageDialog(this, "La venta fue cancelada exitosamente.");
                
            }
        }
    }//GEN-LAST:event_deleteSaleButtonMouseClicked



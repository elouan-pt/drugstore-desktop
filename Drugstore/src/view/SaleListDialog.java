    private void fillTable() {
        proofOfPaymentTextField.setText(search);
        if (list != null) {
            while (dtm.getRowCount() > 0) {
                dtm.removeRow(0);
            }
            for (SaleProductDetail spd : list) {
                double total = spd.getSalePrice() * spd.getQuantity();
                dtm.addRow(new Object[]{spd.getProduct().getDescription(), spd.getProduct().getForm(), spd.getQuantity(), spd.getSalePrice(), total});
            }
            subtotalTextField.setText(sale.getSubtotal() + "");
            igvTextField.setText(sale.getIgv() + "");
            totalTextField.setText(sale.getTotal() + "");
            dateChooser.setDate(sale.getSaleDate());
        }

    }
